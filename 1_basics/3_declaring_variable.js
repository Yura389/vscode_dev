/**
 * Variable 선언하기
 * 
 * 1) var - 더이상 쓰지 않는다.
 * 2) let 
 * 3) const
 */

var name = '홍길동';
console.log(name);

var age = 28;
console.log(age);

let bts = '방탄소년단';
console.log(bts);

/**
 * let과 var로 선언하면
 * 값을 추후 변결할 수 있다.
 */

bts = '박지민';
console.log(bts);

const Bts = '방탄소년단';
console.log(Bts);

/**
 * 선언과 할당
 * 
 * 1) 변수를 선언하는 것.
 * 2) 할당
 */

var name = '프론트엔드';
console.log(name);

let girlfriend;
console.log(girlfriend);


